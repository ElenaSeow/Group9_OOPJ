    private void SaveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveBtnActionPerformed
        // TODO add your handling code here:
        String visitorid = VisitorID.getText();
        String userid = UserID.getText();
        String name= Name.getText();
        String contactno = ContactNo.getText();
        String plateno = PlateNo.getText();
        String status =statusBox.getSelectedItem().toString();
        
        if(visitorid.isEmpty() && userid.isEmpty() && name.isEmpty() && contactno.isEmpty() && plateno.isEmpty()){
            JOptionPane.showMessageDialog(null, "Please fill in all the data!");
        }else{
            visitorpass = new VisitorPass(visitorid,userid,name,contactno,plateno,status).Update(visitorpass, visitorid);
            JOptionPane.showMessageDialog(null, "Successfully Updated");
            Security_Main SM = new Security_Main(Session);
            SM.setVisible(true);
            dispose();
        }
    }//GEN-LAST:event_SaveBtnActionPerformed

