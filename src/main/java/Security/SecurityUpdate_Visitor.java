    public void spamdata(String Id) {
        ArrayList<String>data;
        data = Functions.Read("VisitorPass.txt");
        for(String str:data) {
            String[] list = str.split(",");
            if(Id.equals(list[0])) {
                VisitorID.setText(list[0]);
                UserID.setText(list[1]);
                Name.setText(list[2]);
                ContactNo.setText(list[3]);
                PlateNo.setText(list[4]);
                Status.setText(list[5]);
            }
        }
    }
    
    

